class Solution {
  public:
    long long countPairs(int n, int arr[], int k) {
        // code here
        long ans=0;
        vector<long> rem(k);
        for(int i=0;i<n;i++){
            rem[arr[i]%k]++;
            }
        for(int j=0;j<k;j++){
            ans+=(rem[j]*(rem[j]-1))/2;
        }
        return ans;
    }
};

